package com.jeroensteenbeeke.worldgen.tectonics;

public class WorldDimension {
	private final int width;

	private final int height;

	public WorldDimension(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

}
